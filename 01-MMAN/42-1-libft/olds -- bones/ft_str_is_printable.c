int	ft_str_is_printable(char *str)
{
	unsigned int	i;
	int				yes;

	i = 0;
	yes = 1;
	while (str[i])
	{
		if (str[i] >= ' ' && str[i] <= '~')
			yes = 1;
		else
			return (0);
		i++;
	}
	return (yes);
}
